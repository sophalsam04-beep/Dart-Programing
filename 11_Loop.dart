// Loop in flutter

void main(List<String> args) {

    // 1. For loop
    for(int i=1;i<=10;i++){
        print(i);
    }

    for(int b = 0;b<=30;b++){
        print(b);
    }

    // while loop
    int x = 1;
    while( x<=10 ){
        print(x);
        x++;
    }

    int g = 1;
    while (g<= 20) {
        print(g);
        g++;
    }

    // Do-while loop
    int r = 1;
    do {
        print(r);
        r++;
    } while (r<=10);

    int u = 1;
    do {
        print(u);
        u+=3;
    } while (u<=20);
}