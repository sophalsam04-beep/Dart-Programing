// Operator in flutter
void main(List<String> args) {
      // Arithmetic operator
      int a = 10;
      int b = 20;
      print(a+b);
      print(a-b);
      print(a*b);
      print(a/b);
      print(a%b);     


      // Assignment operator
      int x = 7;
      print(x +=3);
      print(x -=3);
      print(x *=3);
      print(x %=3);

      // Comparision Operator
      int y = 3;
      int z =4;

      print(y > z);
      print(y<z);
      print(y!=z);
      print(y==z);
      print(y>=z);
      print(y<=z);

      // Logical opeator
      bool t = true;
      bool f = false;

      print(t && f);
      print(t || f);
      print(!t);

      // increment and decrements
      int count = 5;
      count ++;

      count--;
      print(count);

}